// Relational Operators

let result : boolean;

let num1: number = 100;
let num2: number = 200;
let num3: number = 100;
let sNum1: number = "100";

result = num1 > num2;
console.log(result);

result = num1 < num2;
console.log(result);

result = num1 >= num2;
console.log(result);

result = num1 <= num2;
console.log(result);

result = num2 != num1;
console.log(result);

result = num3 == num1;
console.log(result);

result = sNum1 == num1;
console.log(result);

result = sNum1 === num1;
console.log(result);