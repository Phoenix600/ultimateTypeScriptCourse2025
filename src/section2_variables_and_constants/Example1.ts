// Primitive Types
// let keyword is used to declare variable
// syntax for creating variables:
// let variableName: type = value;
// data types: number, string, boolean,
let numberExample: number = 42;
let stringExample: string = "Hello, TypeScript!";
let booleanExample: boolean = true;

// Using the types
console.log("Number:", numberExample);
console.log("String:", stringExample);
console.log("Boolean:", booleanExample);


// const keyword is used to create constant variables
// syntax for creating constant variables:
// const variableName: type = value;
// once the value is assigned, it cannot be changed.
const numberExample2: number = 42;
const stringExample2: string = "Hello, TypeScript!";
const booleanExample2: boolean = true;

console.log(numberExample2);
console.log(stringExample2);
console.log(booleanExample2);

